
console.log(Math.abs(-5)); // 5 => The absolute value of -5
console.log(Math.sqrt(4)); // 2 => The square root of 4
console.log(Math.pow(2, 3)); // 8 => 2 raised to the power of 3
console.log(Math.round(10.5)); // 11 => Rounds to the nearest integer (10.5 rounds up to 11)
console.log(Math.ceil(10.4)); // 11 => Rounds up to the nearest integer
console.log(Math.floor(10.9)); // 10 => Rounds down to the nearest integer

console.log(Math.min(1, 2, 3, 4, 5, 6)); // 1 => Returns the smallest number in the given set
console.log(Math.max(12, 3, 4, 6, 7, 9, 8, 4)); // 12 => Returns the largest number in the given set