// Declaring a variable using var (function-scoped)
var x;
x = 10; // Can be reassigned and redeclared within the same scope

// Declaring a variable using let (block-scoped)
let y;
y = 20; // Can be reassigned but cannot be redeclared within the same scope

// Declaring a constant using const (block-scoped)
const v = 30; // Cannot be reassigned after initialization
